def main():
    con = sqlite3.connect(DB)
    cur = con.cursor()

    chr_inserted = 0
    for (ord_, year, title, body, era, faction) in CHRONICLE_ADDITIONS:
        cur.execute('DELETE FROM chronicle WHERE ord=?', (ord_,))
        cur.execute(
            'INSERT INTO chronicle(ord, year_label, title, body, era_tag, faction_tag) '
            'VALUES (?,?,?,?,?,?)', (ord_, year, title, body, era, faction))
        chr_inserted += 1

    org_inserted = 0
    for (child, parent, kind, started, ended, notes, faction) in ORG_TREE_ADDITIONS:
        cur.execute('DELETE FROM org_tree WHERE child=?', (child,))
        cur.execute(
            'INSERT INTO org_tree(child, parent, kind, started, ended, notes, faction_tag) '
            'VALUES (?,?,?,?,?,?,?)', (child, parent, kind, started, ended, notes, faction))
        org_inserted += 1

    con.commit()
    print(f'phase31: +{chr_inserted} chronicle, +{org_inserted} org_tree')
    con.close()


if __name__ == '__main__':
    main()
