def main():
    con = sqlite3.connect(DB)
    cur = con.cursor()
    updated = 0; missing = []
    for slug, new_notes in SITE_NOTES_UPDATES.items():
        if not new_notes.strip():
            continue
        n = cur.execute('UPDATE site SET notes=? WHERE slug=?', (new_notes, slug)).rowcount
        if n == 0:
            missing.append(slug)
        else:
            updated += n
    con.commit()
    print(f'phase29_specific_addresses: updated {updated} site notes')
    if missing:
        print(f'  WARN: missing slugs (not in DB): {missing}')
    con.close()


if __name__ == '__main__':
    main()
