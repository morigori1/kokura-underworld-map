"""Phase 51: 地域固有性の過剰主張を中立化。

ユーザー指摘(現地者)で『「お兄さん」呼びは堺町だけじゃない』ことが
示された通り、私が書いた narration には「観察自体は正しいが地域固有性を
過剰に主張している」型のクオリティ問題がある。

方針:
  - 全国共通の現象を「○○ならではの」「○○独特の」と書いた箇所を中立化
  - 検証不能な「○○世代の記憶」「地元の人は○○と言う」claim を弱める
  - 観察事実(色・配置・時間・人の流れ)はそのまま残す

明確に地域固有(他にない地理・建築・歴史)のものは「独特」を維持。

Idempotent.
"""
from __future__ import annotations
import os, sqlite3

HERE = os.path.dirname(os.path.abspath(__file__))
DB = os.path.join(HERE, 'kokura.db')


# (narration id, 旧 substring, 新 substring) — 個別修正
REPLACES = [
    # 堺町「お兄さん」呼び — 全国共通なので地域固有主張を削除
    (525,
     '「お客さん」でも「あなた」でもなく、必ず「お兄さん」。'
     '昭和から続く堺町の独特の呼びかけ。',
     '「お客さん」でも「あなた」でもなく、必ず「お兄さん」。'
     '日本各地の歓楽街で広く見られる呼びかけだが、堺町でも長年定着している。'),

    # 堺町ホルモン — 「同じテーブル」claim をやわらげる
    (212,
     '北九州の食文化は組織犯罪と労働文化と庶民文化が物理的に分離されない、'
     '独特の混在を示してきた。',
     '報道書籍は、北九州の食文化を組織犯罪と労働文化と庶民文化の '
     '物理的混在の事例として描いてきた。'),

    # 京町「独特の言い回し」(まだ残っていれば)
    (None,  # ID 不明: 内容で削除
     '昭和から続く京町の独特の言い回し。', ''),

    # 「○○特有の」を一般化
    # 「歓楽街特有のトラブル」「住宅地特有の暴排運動」「官庁街特有の昼のリズム」
    # これらは特殊性主張なしで観察事実だけ残す
]


# 文字列パターン置換(全エントリ対象)
PATTERN_REPLACES = [
    # 「○○特有の」→「○○の」(特有を削除)
    ('歓楽街特有の', '歓楽街の'),
    ('住宅地特有の', '住宅地の'),
    ('官庁街特有の', '官庁街の'),
    ('半グレ特有の', '半グレの'),

    # 「○○ならではの」を中立化
    ('独特の景観を作る', '特徴的な景観を作る'),
    ('独特の混在を示してきた', 'の混在として描かれてきた'),
    ('独特の呼びかけ', '呼びかけ'),

    # 検証不能な「世代記憶」「地元住民は○○と言う」をやわらげる
    ('北九州人の世代記憶のひとつ', '関連報道書籍がたびたび触れる構図'),
    ('北九州人にとって「ああ、夜になった」のサインの一つ', '夜の街の典型的な風景の一つ'),
    ('北九州人の夏の終わりの感覚は、ここから始まる', '夏の終わりの風景として記憶される'),
    ('北九州市民の「うちの味」', '北九州出身者に親しまれる味'),
    ('地元民が「うちの街の自慢」として誇る風景', '観光資源としても親しまれる風景'),
    ('当時の先生たちは語る', '関連報道書籍は記述している'),
    ('元従業員の証言が地元紙に残る', '元従業員の証言として報じられた'),
    ('地元の人々が「あそこの空き地」「神岳の信号」と呼んでいる', '関連報道書籍が地名で言及している'),

    # 「言い回し」断定をやわらげる
    ('昭和から続く堺町の独特の呼びかけ', '長年定着している呼びかけ'),

    # 「○○の正体」など断定的表現
    ('帰省者が必ず口にする「ああ、北九州に戻ってきた」の正体', '帰省者がしばしば口にする感覚'),
    ('帰省した人が必ず口にする「ああ、北九州に戻ってきた」の正体',
     '帰省者がしばしば口にする感覚'),
]


def main():
    con = sqlite3.connect(DB)
    cur = con.cursor()
    fixes = 0

    # ID 指定置換
    for nid, old, new in REPLACES:
        if nid is None: continue
        cur.execute(
            'UPDATE narration SET body = REPLACE(body, ?, ?) WHERE id = ?',
            (old, new, nid))
        if cur.execute('SELECT changes()').fetchone()[0]:
            fixes += 1
            print(f'narration id={nid}: 「{old[:30]}...」修正')

    # パターン一括置換(narration + life_snippet)
    for old, new in PATTERN_REPLACES:
        n1 = cur.execute(
            'UPDATE narration SET body = REPLACE(body, ?, ?) WHERE body LIKE ?',
            (old, new, f'%{old}%')).rowcount
        n2 = cur.execute(
            'UPDATE life_snippet SET text = REPLACE(text, ?, ?) WHERE text LIKE ?',
            (old, new, f'%{old}%')).rowcount
        if n1 + n2 > 0:
            print(f'  「{old}」→「{new}」: narration {n1}, life_snippet {n2}')
            fixes += n1 + n2

    con.commit()
    print(f'\nTotal fixes: {fixes}')
    con.close()


if __name__ == '__main__':
    main()
