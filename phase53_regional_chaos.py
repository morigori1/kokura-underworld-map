def main():
    con = sqlite3.connect(DB)
    cur = con.cursor()

    # Place lookup
    place_ids = {}
    for r in cur.execute('SELECT id, name_canonical FROM place').fetchall():
        place_ids[r[1]] = r[0]

    # Insert new sites
    new_count = 0
    for tpl in NEW_SITES:
        (slug, label, lat, lon, unc, kind, fs, ls, notes, era, faction, pc) = tpl
        if cur.execute('SELECT id FROM site WHERE slug = ?', (slug,)).fetchone():
            continue
        pref, city = pc
        # Try to find a matching place row; otherwise NULL
        pid = None
        for pname in (city, pref):
            if pname and pname in place_ids:
                pid = place_ids[pname]
                break
        cur.execute(
            'INSERT INTO site(slug, label, place_id, rep_lat, rep_lon, uncertainty_m, '
            ' kind, first_seen, last_seen, status, notes, era_tag, faction_tag) '
            'VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?)',
            (slug, label, pid, lat, lon, unc, kind, fs, ls, 'active', notes, era, faction))
        new_count += 1
    con.commit()

    # narration
    sites = {r[1]: r[0] for r in cur.execute('SELECT id, slug FROM site').fetchall()}
    n_added = 0
    for slug, rows in NARRATIONS.items():
        sid = sites.get(slug)
        if sid is None: continue
        for ord_, title, body in rows:
            cur.execute('DELETE FROM narration WHERE site_id=? AND ord=?', (sid, ord_))
            cur.execute(
                'INSERT INTO narration(site_id, ord, title, body) VALUES (?,?,?,?)',
                (sid, ord_, title, body))
            n_added += 1
    con.commit()
    print(f'phase53_regional_chaos: +{new_count} sites, +{n_added} narration')
    print(f'  total sites: {cur.execute("SELECT COUNT(*) FROM site").fetchone()[0]}')
    con.close()


if __name__ == '__main__':
    main()
