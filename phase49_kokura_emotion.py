def main():
    con = sqlite3.connect(DB)
    cur = con.cursor()
    s_ids = {row[0]: row[1] for row in cur.execute('SELECT slug, id FROM site')}

    nr_added = 0; ls_added = 0; missing = []
    for slug, rows in NARRATION_ADD.items():
        sid = s_ids.get(slug)
        if sid is None: missing.append(slug); continue
        for ord_, title, body in rows:
            cur.execute('DELETE FROM narration WHERE site_id=? AND ord=?', (sid, ord_))
            cur.execute(
                'INSERT INTO narration(site_id, ord, title, body) VALUES (?,?,?,?)',
                (sid, ord_, title, body))
            nr_added += 1
    for slug, rows in LIFE_SNIPPETS_ADD.items():
        sid = s_ids.get(slug)
        if sid is None: missing.append(slug); continue
        for ord_, topic, text, label, url in rows:
            cur.execute('DELETE FROM life_snippet WHERE site_id=? AND ord=?', (sid, ord_))
            cur.execute(
                'INSERT INTO life_snippet(site_id, ord, topic, text, source_label, source_url) '
                'VALUES (?,?,?,?,?,?)',
                (sid, ord_, topic, text, label, url))
            ls_added += 1
    con.commit()
    print(f'phase49_kokura_emotion: +{nr_added} narration, +{ls_added} life_snippets')
    if missing: print(f'  WARN: missing slugs: {sorted(set(missing))}')
    con.close()


if __name__ == '__main__':
    main()
