def main():
    con = sqlite3.connect(DB)
    cur = con.cursor()
    s_ids = {row[0]: row[1] for row in cur.execute('SELECT slug, id FROM site')}
    ls_inserted = 0; missing = []
    for slug, rows in LIFE_SNIPPETS.items():
        if not rows: continue
        sid = s_ids.get(slug)
        if sid is None:
            missing.append(slug); continue
        cur.execute('DELETE FROM life_snippet WHERE site_id = ?', (sid,))
        for ord_, topic, text, label, url in rows:
            cur.execute(
                'INSERT INTO life_snippet(site_id, ord, topic, text, source_label, source_url) '
                'VALUES (?,?,?,?,?,?)',
                (sid, ord_, topic, text, label, url))
            ls_inserted += 1
    con.commit()
    total = cur.execute('SELECT COUNT(*) FROM life_snippet').fetchone()[0]
    sites = cur.execute('SELECT COUNT(DISTINCT site_id) FROM life_snippet').fetchone()[0]
    print(f'phase40_life_extend: +{ls_inserted} life_snippets')
    print(f'  totals: life_snippet={total} across {sites}/227 sites')
    if missing: print(f'  WARN: missing slugs: {missing}')
    con.close()


if __name__ == '__main__':
    main()
