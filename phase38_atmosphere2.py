def main():
    con = sqlite3.connect(DB)
    cur = con.cursor()
    s_ids = {row[0]: row[1] for row in cur.execute('SELECT slug, id FROM site')}
    nr_inserted = 0; missing = []
    for slug, rows in NARRATION.items():
        if not rows: continue
        sid = s_ids.get(slug)
        if sid is None:
            missing.append(slug); continue
        cur.execute('DELETE FROM narration WHERE site_id = ?', (sid,))
        for ord_, title, body in rows:
            cur.execute(
                'INSERT INTO narration(site_id, ord, title, body) VALUES (?,?,?,?)',
                (sid, ord_, title, body))
            nr_inserted += 1
    con.commit()
    print(f'phase38_atmosphere2: +{nr_inserted} narration entries across '
          f'{sum(1 for v in NARRATION.values() if v)} sites')
    if missing: print(f'  WARN: missing slugs: {missing}')
    con.close()


if __name__ == '__main__':
    main()
